// decode_token
function DecodeToken($token){
    $jwt = parse_ini_file($_SERVER['DOCUMENT_ROOT'] . 'Model/JWT.ini');
    $secret = $jwt['secret'];

    $JWT = new JWT;
    $token_dec = $JWT->decode($token, $secret);
    $rt_token = json_decode($token_dec, TRUE);
    return $rt_token;
}
