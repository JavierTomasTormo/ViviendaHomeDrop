//create_token
function CreateToken($username){
    $jwt = parse_ini_file($_SERVER['DOCUMENT_ROOT'] . 'Model/JWT.ini');
    $header = $jwt['header'];
    $secret = $jwt['secret'];
    $payload = '{"iat":"' . time() . '","exp":"' . time() + (600) . '","username":"' . $username . '"}';

    $JWT = new JWT;
    $token = $JWT->encode($header, $payload, $secret);
    return $token;
}
